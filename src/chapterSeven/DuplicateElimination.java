package chapterSeven;

import java.util.Arrays;

public class DuplicateElimination {

    public static void main(String[] args) {
        int [] array = new int[10];
        for (int i = 0; i < array.length; i++) {
            array [i] = -1;
            System.out.print(array[i]+ " ");
        }
        System.out.println();
    }
}

